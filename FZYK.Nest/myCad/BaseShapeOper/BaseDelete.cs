using myCad.CADInterfaceCtrl;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace myCad.BaseShapeOper
{
    class BaseDelete
    {

        /// <summary>
        /// 基本删除
        /// </summary>
        /// <param name="deleteID"></param>
        public void detele(int deleteID)
        {
            int index = -99;
            for (int i = 0; i < CADInterface.currentShapes.Count; i++)
            {
                if (CADInterface.currentShapes[i].ShapeID == deleteID)
                {
                    index = i;
                    break;
                }
            }
            if (!(index == -99))
            {
                CADInterface.currentShapes.RemoveAt(index);
            }
        }
    }
}
